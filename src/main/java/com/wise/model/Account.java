public class Account {
	
	private String lifnr;
	private String bukrs;
	private String itemcta;
	private String ktopl;
	private String hkont;
	
	public String getLifnr() {
		return lifnr;
	}
	public void setLifnr(String lifnr) {
		this.lifnr = lifnr;
	}
	public String getBukrs() {
		return bukrs;
	}
	public void setBukrs(String bukrs) {
		this.bukrs = bukrs;
	}
	public String getItemcta() {
		return itemcta;
	}
	public void setItemcta(String itemcta) {
		this.itemcta = itemcta;
	}
	public String getKtopl() {
		return ktopl;
	}
	public void setKtopl(String ktopl) {
		this.ktopl = ktopl;
	}
	public String getHkont() {
		return hkont;
	}
	public void setHkont(String hkont) {
		this.hkont = hkont;
	}

}
