package com.wise.model;

public class NotificationDto {
	
	String cuerpo;
	String estatus;
	String fecha;
	String idioma;
	String titulo;
	String naviso;
	String bukrs;
	String idAviso;
	String estatusLectura;
	public String getCuerpo() {
		return cuerpo;
	}
	public void setCuerpo(String cuerpo) {
		this.cuerpo = cuerpo;
	}
	public String getEstatus() {
		return estatus;
	}
	public void setEstatus(String estatus) {
		this.estatus = estatus;
	}
	public String getFecha() {
		return fecha;
	}
	public void setFecha(String fecha) {
		this.fecha = fecha;
	}
	public String getIdioma() {
		return idioma;
	}
	public void setIdioma(String idioma) {
		this.idioma = idioma;
	}
	public String getTitulo() {
		return titulo;
	}
	public void setTitulo(String titulo) {
		this.titulo = titulo;
	}
	public String getNaviso() {
		return naviso;
	}
	public void setNaviso(String naviso) {
		this.naviso = naviso;
	}
	public String getBukrs() {
		return bukrs;
	}
	public void setBukrs(String bukrs) {
		this.bukrs = bukrs;
	}
	public String getIdAviso() {
		return idAviso;
	}
	public void setIdAviso(String idAviso) {
		this.idAviso = idAviso;
	}
	public String getEstatusLectura() {
		return estatusLectura;
	}
	public void setEstatusLectura(String estatusLectura) {
		this.estatusLectura = estatusLectura;
	}

}
